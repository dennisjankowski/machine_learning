import numpy as np
from pyKriging import kriging
X = np.array([
       [0.75,  0.15],
       [0.55,  0.95],
       [0.15,  0.25],
       [0.25,  0.85],
       [0.05,  0.65],
       [0.35,  0.45],
       [0.45,  0.05],
       [0.85,  0.75],
       [0.65,  0.55],
       [0.95,  0.35]])

y = np.array([0.865, 0.325, 0.085, 0.065 ,0.125, 0.325, 0.925, 1.145, 1.325, 0.9025])

k = kriging(X, y)
k.train()
print(k.predict([.25,.25]))
k.plot()
